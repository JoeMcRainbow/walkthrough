function loadJB(jailbreak){
    if (jailbreak == 142){
        window.location.href = 'https://yltboi.me/walkthrough/jailbreak/checkra1n.html'
    }
    else if (jailbreak == 143 || jailbreak == 144){
        window.location.href = 'https://yltboi.me/walkthrough/jailbreak/unc0ver.html'
    }
    else if (jailbreak == 143){
        window.location.href = 'https://yltboi.me/walkthrough/jailbreak/taurine.html'
    }
    else if (jailbreak == 'Odyssey'){
        window.location.href = 'https://yltboi.me/walkthrough/jailbreak/odyssey.html'
    }
}